function changeName(){
    var x = document.getElementById("name-change");
    if(x.innerHTML == "SHANGYUN"){
        x.innerHTML = "上耘";
    }
    else{
        x.innerHTML = "SHANGYUN";
    }
}

function changeFace(){
    var x = document.getElementById("face-change");
    if(x.innerHTML == "• - •"){
        x.innerHTML = "• ω •";
    }
    else{
        x.innerHTML = "• - •";
    }
}